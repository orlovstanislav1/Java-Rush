package JR5.JR1_31;

/*
Риуем линии
 */
public class Main {
    public static void main(String[] args) {
        int i, j;
        for (i = 0; i < 9; i++) { // в ширину
            System.out.print(8);
        }
        for (j = 0; j <= 10; j++) { // в высоту
            System.out.println(8);
        }
        System.out.println();
    }
}

